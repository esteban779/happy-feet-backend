package com.happyfeet.model;

public class ProveedorModel {

	private String id_prove;
	private String nomb_prove;
	private String ruc;
	private String nomb_cont;
	private String email_cont;
	private String telf_cont;
	private String direccion;
	
	public String getId_prove() {
		return id_prove;
	}
	public void setId_prove(String id_prove) {
		this.id_prove = id_prove;
	}
	public String getNomb_prove() {
		return nomb_prove;
	}
	public void setNomb_prove(String nomb_prove) {
		this.nomb_prove = nomb_prove;
	}
	public String getRuc() {
		return ruc;
	}
	public void setRuc(String ruc) {
		this.ruc = ruc;
	}
	public String getNomb_cont() {
		return nomb_cont;
	}
	public void setNomb_cont(String nomb_cont) {
		this.nomb_cont = nomb_cont;
	}
	public String getEmail_cont() {
		return email_cont;
	}
	public void setEmail_cont(String email_cont) {
		this.email_cont = email_cont;
	}
	public String getTelf_cont() {
		return telf_cont;
	}
	public void setTelf_cont(String telf_cont) {
		this.telf_cont = telf_cont;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
}

package com.happyfeet.model;

public class PedidoModel {

	private String id_pedi;
	private String id_clie;
	private String fecha;
	
	public String getId_pedi() {
		return id_pedi;
	}
	public void setId_pedi(String id_pedi) {
		this.id_pedi = id_pedi;
	}
	public String getId_clie() {
		return id_clie;
	}
	public void setId_clie(String id_clie) {
		this.id_clie = id_clie;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
}

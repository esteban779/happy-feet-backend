package com.happyfeet.model;

public class TrabajadorModel {

	private String id_trab;
	private String id_usua;
	private String nomb_trab;
	private String apel_trab;
	private String fech_ingr;
	
	public String getId_trab() {
		return id_trab;
	}
	public void setId_trab(String id_trab) {
		this.id_trab = id_trab;
	}
	public String getId_usua() {
		return id_usua;
	}
	public void setId_usua(String id_usua) {
		this.id_usua = id_usua;
	}
	public String getNomb_trab() {
		return nomb_trab;
	}
	public void setNomb_trab(String nomb_trab) {
		this.nomb_trab = nomb_trab;
	}
	public String getApel_trab() {
		return apel_trab;
	}
	public void setApel_trab(String apel_trab) {
		this.apel_trab = apel_trab;
	}
	public String getFech_ingr() {
		return fech_ingr;
	}
	public void setFech_ingr(String fech_ingr) {
		this.fech_ingr = fech_ingr;
	}
}

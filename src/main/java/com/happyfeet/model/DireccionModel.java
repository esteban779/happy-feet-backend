package com.happyfeet.model;

public class DireccionModel {

	private String id_dire;
	private String id_clie;
	private String id_tipo_dire;
	private String direccion;
	
	public String getId_dire() {
		return id_dire;
	}
	public void setId_dire(String id_dire) {
		this.id_dire = id_dire;
	}
	public String getId_clie() {
		return id_clie;
	}
	public void setId_clie(String id_clie) {
		this.id_clie = id_clie;
	}
	public String getId_tipo_dire() {
		return id_tipo_dire;
	}
	public void setId_tipo_dire(String id_tipo_dire) {
		this.id_tipo_dire = id_tipo_dire;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
}

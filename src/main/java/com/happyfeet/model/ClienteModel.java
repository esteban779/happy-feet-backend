package com.happyfeet.model;

public class ClienteModel {

	private String id_clie;
	private String id_usua;
	private String id_tipo_docu;
	private String nomb_clie;
	private String apel_clie;
	private String docu_clie;
	private String email;
	private String telefono;
	
	public String getId_clie() {
		return id_clie;
	}
	public void setId_clie(String id_clie) {
		this.id_clie = id_clie;
	}
	public String getId_usua() {
		return id_usua;
	}
	public void setId_usua(String id_usua) {
		this.id_usua = id_usua;
	}
	public String getId_tipo_docu() {
		return id_tipo_docu;
	}
	public void setId_tipo_docu(String id_tipo_docu) {
		this.id_tipo_docu = id_tipo_docu;
	}
	public String getNomb_clie() {
		return nomb_clie;
	}
	public void setNomb_clie(String nomb_clie) {
		this.nomb_clie = nomb_clie;
	}
	public String getApel_clie() {
		return apel_clie;
	}
	public void setApel_clie(String apel_clie) {
		this.apel_clie = apel_clie;
	}
	public String getDocu_clie() {
		return docu_clie;
	}
	public void setDocu_clie(String docu_clie) {
		this.docu_clie = docu_clie;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
}

package com.happyfeet.model;

public class TrabajadorPedidoModel {

	private String id_trab_pedi;
	private String id_trab;
	private String id_pedi;
	private String fecha_asig;
	private String id_estd;
	
	public String getId_trab_pedi() {
		return id_trab_pedi;
	}
	public void setId_trab_pedi(String id_trab_pedi) {
		this.id_trab_pedi = id_trab_pedi;
	}
	public String getId_trab() {
		return id_trab;
	}
	public void setId_trab(String id_trab) {
		this.id_trab = id_trab;
	}
	public String getId_pedi() {
		return id_pedi;
	}
	public void setId_pedi(String id_pedi) {
		this.id_pedi = id_pedi;
	}
	public String getFecha_asig() {
		return fecha_asig;
	}
	public void setFecha_asig(String fecha_asig) {
		this.fecha_asig = fecha_asig;
	}
	public String getId_estd() {
		return id_estd;
	}
	public void setId_estd(String id_estd) {
		this.id_estd = id_estd;
	}
}
